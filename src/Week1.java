public class Week1 {

    // public static void main(String args[])
    // 자바 프로그램이 실행되는 시작점입니다.
    public static void main(String[] args) {

        // 1주차: "Hello World!" 출력
        System.out.println("Hello, World!");

        // 1주차 과제: println() 한 번으로 여러 줄 출력하기
        // '\n'은 줄바꿈을 의미하는 특수문자입니다.
        System.out.println("자바를\n잡아라!");
    }
}